#include <unistd.h>

void	ft_putchar(char c)
{
	write (1, &c, 1);
}

void	ft_putnbr(int nb)
{
	int	flag;

	flag = 1;
	if (nb == -2147483648)
	{
		nb = -214748364;
		flag = 0;
	}
	if (nb < 0)
	{
		ft_putchar('-');
		nb = -nb;
	}
	if (nb > 9)
		ft_putnbr(nb / 10);
	ft_putchar(nb % 10 + '0');
	if (flag == 0)
		ft_putchar('8');
}
